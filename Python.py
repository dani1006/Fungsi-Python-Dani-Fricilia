def luas_bujur_sangkar(sisi) :
    luas = sisi * sisi
    return luas
def keliling_bujur_sangkar(sisi) :
    keliling = 4 * sisi
    return keliling
def luas_persegi_panjang(panjang,lebar) :
    luas = panjang * lebar
    return luas
def keliling_persegi_panjang(panjang,lebar):
    keliling = 2 * (panjang + lebar) 
    return keliling
